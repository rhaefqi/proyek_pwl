    <h2>PEMBAGIAN BANTUAN LANGSUNG TUNAI BULAN APRIL DI DESA SUKAMAJU</h2><br>
    <div ><img  class="gambar1" src="img/contohsidang.jpeg" alt=""></div><br>
    <p>Pencairan Bantuan Langsung Tunai Dana Desa bulan April tahun 2022 di laksanakan pada hari Selasa tanggal 26 April 2022 bertempat di Balai Desa Sukamaju pukul 09.00 WIB sampai dengan selesai. Kegiatan ini merupakan pencairan BLT yang ke empat di tahun 2022.</p>

    <p>Pelaksanaan pencairan BLT-DD bulan September ini dengan Jumlah 96 KPM (Keluarga Penerima Manfaat) dengan syarat    pengambilan membawa foto copy Kartu Keluarga (KK), foto copy Kartu Tanda Penduduk (KTP) dan Surat undangan dariPemerintah Desa Babagan. Dengan menerapkan Protokol Kesehatan KPM ini juga bersabar untuk antri dan tertib dalam penerimaan BLT-DD yang akan diterimanya sehingga pelaksanaan bisa berjalan dengan baik dan lancar.</p>
   <p>Adapun syarat pengambilan bantuan tersebut sesuai surat undangan yang dikeluarkan oleh Pemerintah Desa    Wangunsari pada tanggal 18 APril 2022 adalah sebagai berikut:

     <p>  1.  Membawa Surat Undangan dari Pemerintah Desa Sukamaju</p>
     <p>  2.  Membawa Fotokopi KK/KTP KPM</p>
     <p>  3.  Pengambilan tidak boleh diwakilkan</p>
     <p>  4.  KPM tidak diperbolehkan memberikan uang atau sejenisnya kepada pihak yang tidak bertanggung jawab.</p>
</p> 
<p>KPM (Keluarga Penerima Manfaat) harus bersyukur karena di tahun setelah covid ini, masyarakat masih menerima Bantuan Langsung Tunai Desa terdampak Covid-19 terkecuali KPM yang meninggal atau mendapatkan bantuan lain, itupun hanya beberapa orang saja. </p> 
<p>Bukan tanpa dasar ibu-ibu dan bapak-bapak masih menerima bantuan ini melainkan kami Pemerintah Desa bekerja keras mengajukan data warga masyarakat yang kurang mampu dengan program bantuan lainnya, alhasil sebanyak 85% warga masyarakat mendapatkan dan merasakan bantuan dari Pemerintah Daerah dan Pemerintah Pusat seperti BLT Kabupaten, Bantuan Sembako dan Uang Tunai dari Provinsi, Bantuan Keluarga Harapan (PKH), Bantuan Pangan Non Tunai (BPNT) dan tambahan bantuan tunai minyak goreng, bantuan dengan pengambilan langsung ke Bank lewat program UMKM dan bantuan Himbara kami rasa berbagai bantuan yang kami sebutkan tadi dapat menutupi kebutuhan masyarakat desa dalam upaya meningkatkan perekonomian masyarakat desa" tegas Kades Rifqy dalam sambutannya.</p> 
